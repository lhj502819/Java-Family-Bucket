package cn.onenine.design.patterns.principle.openclosed;

/**
 * Description：对扩展开放，修改关闭原则：software entities(modules,classes,functions,etc)should be open for extension but closed for modification
 *                      软件实体（模块、类、方法等）应该“对扩展开放，对修改关闭”
 *
 * @author li.hongjian
 * @email lhj502819@163.com
 * @since 2021/8/19
 */
public class OpenClosedPrinciple {
    /**
     * 添加一个新的功能应该是在已有代码基础上扩展代码（新增模块、类、方法等），而非修改已有代码（修改模块、类、方法等）
     */

}
